#convert list to map based on given attribute
def toMap(list,key):
    map = {}
    index = 0
    for item in list:
        map[item[key]]=item
        item["index"] = index
        index += 1
        #print str(item[key]) + "=" + str(item)
        
    return map

def toMapFunc(list,func):
    map = {}
    index = 0
    for item in list:
        map[func(item)]=item
        item["index"] = index
        index += 1
        #print str(item[key]) + "=" + str(item)
        
    return map

def toMapKeyFunc(list,key_func,func):
    map = {}
    index = 0
    for item in list:
        map[key_func(item)]=func(item)
        item["index"] = index
        index += 1
        #print str(item[key]) + "=" + str(item)
        
    return map


